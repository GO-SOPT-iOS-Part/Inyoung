import UIKit

extension UIColor {
    
    @nonobjc class var tvingWhite: UIColor {
        return UIColor(red: 1, green: 1, blue: 1, alpha: 1)
    }
    
    @nonobjc class var tvingBlack: UIColor {
        return UIColor(red: 0, green: 0, blue: 0, alpha: 1)
    }
    
    @nonobjc class var tvingRed: UIColor {
        return UIColor(red: 1, green: 0.078, blue: 0.235, alpha: 1)
    }
    
    @nonobjc class var tvingGray1: UIColor {
        return UIColor(red: 0.839, green: 0.839, blue: 0.839, alpha: 1)
    }
    
    @nonobjc class var tvingGray2: UIColor {
        return UIColor(red: 0.611, green: 0.611, blue: 0.611, alpha: 1)
    }
    
    @nonobjc class var tvingGray3: UIColor {
        return UIColor(red: 0.384, green: 0.384, blue: 0.384, alpha: 1)
    }
    
    @nonobjc class var tvingGray4: UIColor {
        return UIColor(red: 0.18, green: 0.18, blue: 0.18, alpha: 1)
    }
}
